# Variables Type
x = 3
x = int(3) # x will be 3 as integer variable (number without decimal, it can be -2, -3, 0, 2, 4, 102)

y = 3.1
y = float(3.1) # y will be 3.1 as float variable (number with decimal -1.2, 0.0, 3.1, 12.0324)

z = 'Ali'
z = str('Ali') # z will be 'Ali' as a string variable (string is just a text, any character inside " " is a text string)

status = True
status = bool(True) # y will be a Boolean

print(type(x)) # <class 'int'>
print(type(y)) # <class 'float'>
print(type(z)) # <class 'str'>
print(type(status)) # <class 'str'>